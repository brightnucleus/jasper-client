<?php

namespace BrightNucleus\JasperClient\SoapTypes\OtherClient;

class Identification
{

    /**
     * @var string
     */
    private $_ = null;

    /**
     * @var string
     */
    private $type = null;

    /**
     * @return string
     */
    public function get_()
    {
        return $this->_;
    }

    /**
     * @return string
     */
    public function getType()
    {
        return $this->type;
    }


}

